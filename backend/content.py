def lessons_for_room(room_id: str):
    return [l for l in LESSONS if l["room"] == room_id]


def get_lesson(lesson_id: str):
    for l in LESSONS:
        if l["id"] == lesson_id:
            return l
    for p in TRAINING_PACKS:
        if p["id"] == lesson_id:
            return p
    return None


def get_room(room_id: str):
    for r in ROOMS:
        if r["id"] == room_id:
            return r
    return None


def lesson_summary(l: dict):
    """Хичээлийн хөнгөн хувилбар (жагсаалтад)."""
    return {
        "id": l["id"], "room": l["room"], "section": l["section"],
        "order": l["order"], "level": l["level"], "duration": l["duration"],
        "xp": l["xp"], "title": l["title"], "subtitle": l["subtitle"],
        "tok": l["tok"]["question"], "exercises": len(l.get("exercises", [])),
        "audio": bool(l.get("tts")),
    }
